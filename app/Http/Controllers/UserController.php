     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validatedData = $request->validate([
            'username' => 'required|max:90',
            'email' => 'required|max:90',
            'password' => 'required|max:90'
        ]);

        User::whereId($id)->update(['username' => $request->username,'email' => $request->email, 'password' => Hash::make($validatedData['password'])]);

        return redirect('/profile')->with('success', 'Has actualitzat les teves dades');
    }

